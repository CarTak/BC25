package Exercicios;

public class Heranca {
    private String nome;
    private String cargo;
    private String aprender;
    private String andar;
    private String comer;

    public Heranca(String nome, String cargo, String aprender, String andar, String comer) {
        this.nome = nome;
        this.cargo = cargo;
        this.aprender = aprender;
        this.andar = andar;
        this.comer = comer;
    }

}
